	//在两个解中选出更好的那个，如果两个解之间存在支配关系，则直接根据支配关系判定，如果互相不支配，
	//就判定谁在更多的目标上表现的好
	public static int is_better_solution(Individual individual1, Individual individual2) {
		int result = 0;

		List<Integer> result_each_object=new ArrayList<>();
		for(int i = 0;i < individual1.objectives.size(); i++) {
			if(individual1.objectives.get(i) < individual2.objectives.get(i)) {
				result_each_object.add(1);
			}
			else if (individual1.objectives.get(i).equals(individual2.objectives.get(i))){
				result_each_object.add(0);
			}
			else {
				result_each_object.add(2);
			}
		}
		//最后判断，如果result_each_object里全是1，则说明解1支配2，
		if(Collections.frequency(result_each_object, 1) == 3) {
			result = 1;
		}
		//如果全是2，则说明解2支配1，
		else if (Collections.frequency(result_each_object, 2) == 3){
			result = 2;
		}
		//否则他们俩之间互不支配,则看谁在更多的目标上表现的好
		else {
			if (Collections.frequency(result_each_object, 1) > Collections.frequency(result_each_object, 2)){
				result = 1;
			}else {
				result = 2;
			}
		}
		return result;
	}

