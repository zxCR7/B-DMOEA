	public static double IGD(List<Individual> pop,List<Individual> true_pareto) {
		double IGD=0.0;
		if(pop.size()==0) {
			return 1000000.0;
		}
		for(int i=0;i<true_pareto.size();i++) {
			double distance=Double.MAX_VALUE;
			for(int j=0;j<pop.size();j++) {
				distance=euclidean_distance(true_pareto.get(i).objectives,pop.get(j).objectives)<distance?euclidean_distance(true_pareto.get(i).objectives,pop.get(j).objectives):distance;
			}
			IGD=IGD+distance;
		}
		return IGD/(double)true_pareto.size();
	}

	public static double euclidean_distance(List<Double> point1,List<Double> point2) {
		double result=0.0;
		for(int i=0;i<point1.size();i++) {
			result=result+(point2.get(i)-point1.get(i))*(point2.get(i)-point1.get(i));
		}
		return Math.sqrt(result);
	}

	public static boolean deleteDir(String path){
		File file = new File(path);
		if(!file.exists()){//判断是否待删除目录是否存在
			System.err.println("The dir are not exists!");
			return false;
		}

		String[] content = file.list();//取得当前目录下所有文件和文件夹
		for(String name : content){
			File temp = new File(path, name);
			if(temp.isDirectory()){//判断是否是目录
				deleteDir(temp.getAbsolutePath());//递归调用，删除目录里的内容
				temp.delete();//删除空目录
			}else{
				if(!temp.delete()){//直接删除文件
					System.err.println("Failed to delete " + name);
				}
			}
		}
		return true;
	}

