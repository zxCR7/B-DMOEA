	//获取数组中某个元素的index
	public static int getIndex(int[] arr, int value) {
		for (int i = 0; i < arr.length; i++) {
			if (arr[i] == value) {
				return i;                  //字符串时，换为 equals
			}
		}
		return -1;//如果未找到返回-1
	}

