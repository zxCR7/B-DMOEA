	public static List<List<Double>> normalizationPoint(List<List<Double>> pf) {

		double max[]= {Double.MIN_VALUE,Double.MIN_VALUE,Double.MIN_VALUE};
		double min[]= {Double.MAX_VALUE,Double.MAX_VALUE,Double.MAX_VALUE};
		getMaxAndMinOPoint(pf,max,min);

		List<List<Double>> result = new ArrayList<>();

		for(int n=0;n<pf.size();n++) {
			//每一个点
			List<Double> ind= pf.get(n);
			List<Double> temp = new ArrayList<>();
			for(int i=0;i<ind.size();i++) {
				//temp.set(i, (ind.get(i)-min[i])/(max[i]-min[i]));
				temp.add((ind.get(i)-min[i])/(max[i]-min[i]));
			}
			result.add(temp);
		}

		return result;

	}

	public static void getMaxAndMinOPoint(List<List<Double>> pf,double max[],double min[]) {

		for(List<Double> ind:pf) {
			for(int i=0;i<ind.size();i++) {
				if(ind.get(i)>max[i]) {
					max[i] = ind.get(i);
				}
				if(ind.get(i)<min[i]) {
					min[i] = ind.get(i);
				}
			}
		}
	}

